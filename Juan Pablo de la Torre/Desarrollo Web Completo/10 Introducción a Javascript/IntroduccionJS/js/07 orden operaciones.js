// DECREMENTOS
let puntaje2 = 10;

// Incremento en 1 ++
puntaje2 += 10;

console.log(puntaje2);
//console.log(puntaje--); //primero muestra el puntaje y luego incrementa
console.log(--puntaje2); //primero incrementa y luego muestra el puntaje